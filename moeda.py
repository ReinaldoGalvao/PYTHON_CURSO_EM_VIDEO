def aumentar(x, y):
    soma = x + y
    return soma

def diminuir(x, y):
    diminuir = x - y
    return diminuir

def dobro(x):
    dobro = x * 2
    return dobro

def metade(x):
    metade = x / 2
    return metade

def porcentagem(x, y):
    porcentagem = x + (x * y /100) 
    return porcentagem

def porcentagemmenos(x, y):
    porcentagem = x - (x * y /100) 
    return porcentagem